#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	char	*dest;

	dest = s1;
	while (*s2)
	{
		*dest = *s2;
		dest++;
		s2++;
	}
	*dest = *s2;
	return (s1);
}
